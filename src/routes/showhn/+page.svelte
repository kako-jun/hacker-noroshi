<div style="padding: 10px 0 10px 40px; font-size: 9pt; line-height: 14pt; color: #000000;">

<p>Show HN は、自分で作ったものをコミュニティに見せるための場です。Show HN の目的は、他の人が触れるものを共有し、フィードバックを得ることです。</p>

<p><b>受け入れられるもの</b></p>

<p>動くソフトウェア、ハードウェアプロジェクト（動画や記事付き）、本のサンプルなど、他の人が実際に試せるものが対象です。</p>

<p><b>対象外のもの</b></p>

<p>ブログ記事、サインアップだけのページ、ニュースレター、リストなどは Show HN の対象外です。通常の投稿として共有してください。</p>

<p><b>投稿する側へ</b></p>

<p>タイトルに「Show HN:」を付けてください。</p>

<p>コミュニティからの質問に答えられるようにしてください。</p>

<p>使用の障壁を最小限にしてください。サインアップが必須だと、多くの人は試さずに去ります。</p>

<p>大きなアップデートは Show HN に投稿してかまいませんが、マイナーバージョンの更新は投稿しないでください。</p>

<p><b>コメントする側へ</b></p>

<p>作者への敬意を持ってください。何かを作って見せることには勇気がいります。</p>

<p>批判する場合は建設的な代替案を提示してください。</p>

<p>作者が学び、改善できるようなフィードバックを心がけてください。</p>

