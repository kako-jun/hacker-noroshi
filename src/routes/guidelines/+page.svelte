<p><b>投稿について</b></p>

<p>ハッカーのろしでオントピックなのは、知的好奇心を満たすもの全般です。特に、優れたハッカーにとって興味深いと思われるものが該当します。それ以外の話題、たとえば政治・芸能・スポーツなどは基本的にオフトピックです。</p>

<p>投稿にあたっては以下のことに気をつけてください:</p>

<p>タイトルを飾らないでください。不必要な大文字、感嘆符、宣伝的な言葉は避けましょう。</p>

<p>二次情報より原典を投稿してください。ブログ記事やニュース記事よりも、元の論文や発表を優先します。</p>

<p>ハッカーのろしを主に宣伝目的で使わないでください。自分の作品を共有するのは歓迎ですが、アカウントの大半が自分のコンテンツの宣伝であれば、それは宣伝目的とみなされます。</p>

<p>タイトルからサイト名を除いてください。読者はドメイン表示で判断できます。</p>

<p>「10の方法」のようなリスト形式のタイトルは簡潔にしてください。</p>

<p>元のタイトルを尊重してください。ミスリーディングでない限り、改変しないでください。</p>

<p>動画やPDFを投稿する場合は、タイトルに [video] や [pdf] のタグを付けてください。</p>

<p>削除した投稿を再投稿しないでください。</p>

<p>upvote の依頼をしないでください。</p>

<p><b>コメントについて</b></p>

<p>親切にしてください。皮肉を言わないでください。好奇心を持って会話しましょう。</p>

<p>人を否定するのではなく、議論に実質的に返答してください。</p>

<p>否定的・懐疑的なだけのコメントは避けてください。批判する場合は、なぜそう思うのかを具体的に説明してください。</p>

<p>AI生成コメントを投稿しないでください。ハッカーのろしは人間の会話の場です。</p>

<p>相手の発言を善意に解釈してください。</p>

<p>政治的なイデオロギー対立を持ち込まないでください。</p>

<p>問題のあるコメントを見かけたら、返信するよりフラグを使ってください。</p>

