<p><b>投稿やコメントにルールはありますか？</b></p>

<p><a href="/guidelines">ガイドライン</a>を参照してください。</p>

<p><b>ストーリーのランキング方法は？</b></p>

<p>基本的な仕組みは、ポイント数を投稿からの経過時間のべき乗で割るというものです。</p>

<p><b>カルマの計算方法は？</b></p>

<p>投稿に対する upvote 数の合計で概算されます。</p>

<p><b>カルマが多いユーザーの投稿は上位に表示されますか？</b></p>

<p>いいえ。</p>

<p><b>downvote の矢印が見えないのはなぜですか？</b></p>

<p>ストーリーに downvote はありません。コメントの downvote は一定のカルマに達すると表示されるようになります。ただし、自分への直接の返信には downvote は表示されません。</p>

<p><b>コメントにどんなフォーマットが使えますか？</b></p>

<p>プレーンテキストのみです。空行で段落を区切ることができます。</p>

<p><b>質問を投稿するには？</b></p>

<p><a href="/submit">submit</a> から URL を空欄のまま投稿してください。タイトルに質問を書くと、Ask HN として表示されます。</p>

<p><b>Ask HN と Show HN とは？</b></p>

<p>Ask HN はコミュニティへの質問です。Show HN は自分で作ったものを見せる場です。<a href="/showhn">Show HN のルール</a>も参照してください。</p>

<p><b>緑色のユーザー名の意味は？</b></p>

<p>新しいアカウントであることを示しています。</p>

<p><b>薄い色のコメントの意味は？</b></p>

<p>downvote されたコメントです。スコアが低いほど薄く表示されます。</p>

<p><b>[flagged] の意味は？</b></p>

<p>ユーザーによってガイドライン違反としてフラグされた投稿やコメントです。</p>

<p><b>[dead] の意味は？</b></p>

<p>ソフトウェアまたはユーザーのフラグによって非表示にされた投稿やコメントです。</p>

<p><b>リポストは可能ですか？</b></p>

<p>過去1年程度で注目されていなければ、少数の再投稿は問題ありません。</p>

<p><b>upvote を依頼してもよいですか？</b></p>

<p>いいえ。upvote の依頼はペナルティの対象です。</p>

<p><b>ハッカーのろしと Hacker News の関係は？</b></p>

<p>無関係です。ハッカーのろしは Hacker News にインスパイアされた日本語コミュニティです。</p>

