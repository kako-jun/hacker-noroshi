<p id="rules"><b>投稿やコメントにルールはありますか？</b></p>

<p><a href="/guidelines">ガイドライン</a>を参照してください。</p>

<p id="hierarchical"><b>ストーリーのランキング方法は？</b></p>

<p>基本的な仕組みは、ポイント数を投稿からの経過時間のべき乗で割るというものです。コメントも同じ方法でランク付けされます。</p>

<p>ランクに影響するその他の要素として、ユーザーによるフラグ、不正対策ソフトウェア、過熱した議論を降格するソフトウェア、アカウントやサイトの重み付け、モデレーターの操作があります。</p>

<p id="karma"><b>カルマの計算方法は？</b></p>

<p>大まかには、投稿に対する upvote 数から downvote 数を引いたものです。完全には一致しません。一部の投票は不正対策ソフトウェアによって除外されます。</p>

<p id="hierarchical3"><b>カルマが多いユーザーの投稿は上位に表示されますか？</b></p>

<p>いいえ。</p>

<p id="hierarchical4"><b>downvote の矢印が見えないのはなぜですか？</b></p>

<p>ストーリーに downvote はありません。コメントの downvote は一定のカルマに達すると表示されるようになりますが、自分への直接の返信には表示されません。</p>

<p id="format"><b>コメントにどんなフォーマットが使えますか？</b></p>

<p>プレーンテキストのみです。空行で段落を区切ることができます。URLはそのまま貼ると自動的にリンクになります。</p>

<p id="ask"><b>質問を投稿するには？</b></p>

<p><a href="/submit">submit</a> ページで URL を空欄のまま投稿してください。タイトルに質問を書くと、Ask HN として表示されます。</p>

<p id="nolink"><b>テキスト投稿にリンクを入れることはできますか？</b></p>

<p>できません。これは投稿者のコメントが特権的な位置に表示されるのを防ぐためです。リンクが必要な場合は、リンクを通常の投稿として送信し、コメントで補足してください。</p>

<p id="show"><b>Ask HN と Show HN とは？</b></p>

<p>Ask HN はコミュニティへの質問やテキスト投稿です。Show HN は自分で作ったものを見せる場です。Show HN には<a href="/showhn">専用のルール</a>があります。</p>

<p id="asknew"><b>投稿が Ask HN や Show HN に表示されないのはなぜですか？</b></p>

<p>すべての Ask HN と Show HN は <a href="/newest">newest</a> に表示されます。ただし、<a href="/ask">ask</a> や <a href="/show">show</a> に表示されるには小さなポイント閾値があります。</p>

<p id="hierarchical7"><b>緑色のユーザー名の意味は？</b></p>

<p>新しいアカウントであることを示しています。</p>

<p id="hierarchical8"><b>薄い色のコメントの意味は？</b></p>

<p>downvote されたコメントです。スコアが低いほど薄く表示されます。タイムスタンプをクリックすると通常表示で読めます。</p>

<p id="flagged"><b>[flagged] の意味は？</b></p>

<p>ユーザーによってガイドライン違反としてフラグされた投稿やコメントです。モデレーターがフラグを付けたり外したりすることもあります。</p>

<p id="cflag"><b>コメントをフラグするには？</b></p>

<p>タイムスタンプをクリックしてコメントのページへ移動し、上部の「flag」リンクをクリックしてください。flag リンクが表示されるには小さなカルマ閾値があります。</p>

<p id="dead"><b>[dead] の意味は？</b></p>

<p>ソフトウェア、ユーザーのフラグ、またはモデレーターによって非表示にされた投稿やコメントです。</p>

<p>dead 投稿は既定では表示されませんが、プロフィールで「showdead」を有効にすると見られるようになります。</p>

<p id="cvouch">dead にされるべきでない投稿を見つけた場合は、その投稿に vouch（保証）できます。タイムスタンプをクリックしてその投稿のページへ移動し、上部の「vouch」をクリックしてください。十分な数のユーザーが vouch すると投稿は復元されます。vouch リンクが表示されるには小さなカルマ閾値があります。</p>

<p id="deleted"><b>[deleted] の意味は？</b></p>

<p>投稿者本人が削除した投稿です。<a href="#dead">dead</a> 投稿とは異なり、showdead を有効にしても表示されません。</p>

<p id="hide"><b>hide リンクは何をしますか？</b></p>

<p>その投稿を自分の表示から取り除きます。他のユーザーには引き続き表示されます。プロフィールの hidden から確認・解除できます。</p>

<p id="hierarchical9"><b>リポストは可能ですか？</b></p>

<p>過去1年程度で注目されていなければ、少数の再投稿は問題ありません。それ以外の重複投稿は埋もれます。</p>

<p>同じ投稿を削除して再投稿するのはやめてください。削除は、そもそも投稿すべきでなかったものに対して使ってください。</p>

<p id="paywalls"><b>ペイウォール記事の投稿は可能ですか？</b></p>

<p>回避策のあるペイウォールサイトの記事を投稿することは問題ありません。</p>

<p>コメントで記事の読み方を尋ねたり、他のユーザーを助けたりすることも構いません。ただし、ペイウォール自体への不満を投稿するのは控えてください。それは話題から外れます。</p>

<p id="upvote"><b>upvote を依頼してもよいですか？</b></p>

<p>いいえ。upvote は読者が自分で興味深いと思って行うべきものです。違反はペナルティやアカウント停止の対象です。</p>

<p id="hierarchical11"><b>コメントを依頼してもよいですか？</b></p>

<p>いいえ。同じ理由です。また、あなた自身の利益にもなりません。ハッカーのろしの読者はこの種の操作に敏感です。</p>

<p id="closed"><b>スレッドにコメントできないのはなぜですか？</b></p>

<p>スレッドは投稿から2週間が経過するか、ソフトウェア・モデレーター・ユーザーのフラグによって非表示にされた場合、新規コメントの受付を終了します。</p>

<p id="whyrank"><b>A の方が多くポイントがあって新しいのに B より下にランクされるのはなぜですか？</b></p>

<p>ランクは投票数と時間だけからは導けません。上の「ストーリーのランキング方法は？」を参照してください。</p>

<p id="delay"><b>プロフィールの delay とは？</b></p>

<p>自分のコメントが他のユーザーに表示されるまでの猶予時間です。分単位で設定でき、最大10分まで指定できます。投稿後にコメントを編集する余裕が生まれます。</p>

<p id="noprocrast"><b>プロフィールの noprocrast とは？</b></p>

<p>ハッカーのろしを使い過ぎないようにするための仕組みです。有効にすると、サイトを閲覧できるのは一度に maxvisit 分までで、その間は minaway 分の間隔を空ける必要があります。既定値は20分と180分で、20分閲覧したら3時間は再アクセスできなくなります。</p>

<p id="forgot"><b>パスワードをリセットするには？</b></p>

<p>プロフィールにメールアドレスを登録している場合は、<a href="/forgot">こちら</a>からリセットできます。登録していない場合はリセットできません。</p>

<p id="changeusername"><b>ユーザー名を変更できますか？</b></p>

<p>はい。プロフィールページの「Change username」から自分で変更できます。ただし<b>90日に1回まで</b>の制限があり、過去に誰かが使ったユーザー名は（自分自身が以前使っていたものも含めて）再利用できません。変更後は旧ユーザー名のプロフィール URL から自動的に新しい URL へリダイレクトされます。</p>

<p id="hierarchical12"><b>ハッカーのろしと Hacker News の関係は？</b></p>

<p>無関係です。ハッカーのろしは Hacker News にインスパイアされた日本語コミュニティです。</p>

